namespace WCA.Core.Services
{
    //
    // Summary:
    //     This enumeration is used by ExceptionTelemetry and TraceTelemetry to identify
    //     severity level.
    public enum WCASeverityLevel
    {
        //
        // Summary:
        //     Verbose severity level.
        Verbose = 0,
        //
        // Summary:
        //     Information severity level.
        Information = 1,
        //
        // Summary:
        //     Warning severity level.
        Warning = 2,
        //
        // Summary:
        //     Error severity level.
        Error = 3,
        //
        // Summary:
        //     Critical severity level.
        Critical = 4
    }
}
